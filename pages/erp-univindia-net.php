        <li>Select the <strong>Examination Year</strong> — for example, Annual Examination 2024.</li>
        <li>Click <strong>"Proceed for Result"</strong>.</li>
        <li>Enter your <strong>Roll Number</strong> and click Submit.</li>
        <li>Your complete result scorecard will appear on the screen. Download it as PDF for future reference.</li>
    </ol>

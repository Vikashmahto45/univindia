                <h2 class="h3 text-info mt-5 mb-3 border-bottom border-secondary pb-2">Steps to Download SSC GD Call Letter Online</h2>
                <p>
                    Follow these precise steps to get your admit card from the official SSC portal:
                </p>
                <ol>
                    <li>Visit the official central website: <a href="https://ssc.gov.in" target="_blank" class="text-info">ssc.gov.in</a> or your regional portal.</li>
                    <li>Navigate to the <strong>'Admit Card'</strong> tab at the top of the header.</li>
                    <li>Select the URL of your specific region (e.g., Eastern, Southern, Central).</li>
                    <li>Click on the link for <strong>"STATUS / DOWNLOAD ADMIT CARD FOR CONSTABLE (GD) 2025"</strong>.</li>
                    <li>Submit your <strong>Registration Number</strong> and <strong>Password/Date of Birth</strong>.</li>
                    <li>If you don't remember your ID, you can use your <strong>Name, Father's Name, and DOB</strong>.</li>
                    <li>The admit card will be displayed after a successful login.</li>
                    <li>Check the timing and center details, then print the PDF.</li>
                </ol>


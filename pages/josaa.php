                <p>
                    After a seat is allotted in Round 1-6, you must choose one of these:
                </p>
                <ul>
                    <li><strong>Freeze:</strong> You are satisfied with the seat and do not want to participate further. You will pay the fee and report.</li>
                    <li><strong>Slide:</strong> You accept the seat but will move to a better branch in the SAME institute if available in next rounds.</li>
                    <li><strong>Float:</strong> You accept the seat but will move to a better institute/branch anywhere in the list in next rounds.</li>
                </ul>


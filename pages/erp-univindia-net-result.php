    <h2>Understanding Your MGSU Result Marksheet on erp.univindia.net</h2>
    <p>When you check your <strong>erp.univindia.net result</strong>, the scorecard displayed on your screen contains several important fields. Here is what each section on the MGSU marksheet means:</p>
    <ul>
        <li><strong>Roll Number:</strong> Your unique examination roll number assigned by the university for that examination year.</li>
        <li><strong>Enrollment Number:</strong> Your permanent student registration ID at MGSU.</li>
        <li><strong>Subject Code and Subject Name:</strong> Every subject you appeared in is listed with its official code and full name.</li>
        <li><strong>Theory Marks and Practical Marks:</strong> Shown separately for subjects that have both components.</li>
        <li><strong>Total Marks and Maximum Marks:</strong> Your obtained marks versus the total possible marks for each subject.</li>
        <li><strong>Status (Pass / Fail / Absent / Grace):</strong> Your final status in each subject. Grace marks of up to 1% of total marks can be applied to help clear a borderline subject.</li>
        <li><strong>Division:</strong> Your overall result grade — First Division (60%+), Second Division (48%–59%), Third Division (36%–47%).</li>
        <li><strong>Percentage:</strong> Your aggregate percentage across all subjects for that part or year.</li>
    </ul>

    <div class="highlight-box">
        <strong>📌 Minimum Passing Marks at MGSU:</strong> Students must score at least <strong>36%</strong> in each theory subject to be declared Pass. In practical subjects, the minimum passing marks may vary. Students who fail in up to two theory papers are eligible to appear in the supplementary examination.
    </div>

    <h2>What to Do After Checking Your erp univindia net Result</h2>
    <p>After successfully checking your <strong>univindia.net result</strong>, here are the important next steps depending on your result status:</p>

    <h3>If You Passed:</h3>
    <ul>
        <li>Download and print the online scorecard from erp.univindia.net for your personal records.</li>
        <li>Collect your official physical marksheet from your affiliated college when it is issued (usually 2–3 months after online result).</li>
        <li>If you completed your final year, apply for your degree certificate through your college or directly on the portal.</li>
        <li>Upload your result on DigiLocker — MGSU is integrated with the National Academic Depository for digital verification of marksheets.</li>
    </ul>

    <h3>If You Failed (in 1–2 Subjects):</h3>
    <ul>
        <li>Check the exact subjects and marks where you failed on the erp.univindia.net result page.</li>
        <li>Apply for the <strong>Supplementary Examination</strong> through your college before the deadline. This allows you to clear failed subjects without losing the academic year.</li>
        <li>Alternatively, apply for <strong>Revaluation</strong> within 15 days of result declaration if you believe your marks are incorrect. Fee is approximately ₹500 per paper.</li>
        <li>Apply for <strong>Scrutiny</strong> at approximately ₹300 per paper if you want your answer sheet rechecked without full revaluation.</li>
    </ul>

    <h3>If There is an Error in Your Result:</h3>
    <ul>
        <li>Immediately contact your affiliated college's principal office with your admit card and enrollment documents.</li>
        <li>The college will forward a correction request to the MGSU Bikaner examination department.</li>
        <li>Common errors include name spelling mistakes, wrong marks entry, and absent-marked despite appearing. All are correctable through official channels.</li>
    </ul>

